// . Даны три числа. Найти наименьшее из них

let a = 51;
let b = 36;
let c = 12;

if (a < b && a < c) {
  console.log("Наименьшее число это а", a);
} else if (b < a && b < c) {
  console.log("Наименьшее число это б", b);
} else if (c < a && c < b) {
  console.log("Наименьшее число это с", c);
}
